package SCJPTestSat;


	public class Creature {
		int wingCount;
		private int legCount;
	    
	    
	    public Creature(int legCount) {
	        this.legCount = this.legCount;
	        this.wingCount = 0;
	    }
	    
	    public String toString() {
	        return "legs=" + this.legCount + " wings=" + wingCount;
	    }
	}


